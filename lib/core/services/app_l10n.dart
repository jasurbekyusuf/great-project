String _resolveBucket(Locale locale) {
  if (locale.languageCode == 'uz' && (locale.scriptCode == 'Cyrl' || locale.countryCode == 'Cyrl')) {
    return 'uz_Cyrl';
  }
  return locale.languageCode;
}

class AppL10n {
  AppL10n(this.locale);
  final Locale locale;

  String tr(String key) {
    final bucket = _resolveBucket(locale);
    return _strings[bucket]?[key] ?? _strings['uz']?[key] ?? key;
  }
}

final appL10nProvider = Provider<AppL10n>((ref) {
  final locale = ref.watch(localeProvider);
  return AppL10n(locale);
});

extension TrX on String {
  String tr(WidgetRef ref) => ref.watch(appL10nProvider).tr(this);
  String trCtx(BuildContext context, WidgetRef ref) => ref.watch(appL10nProvider).tr(this);
}
